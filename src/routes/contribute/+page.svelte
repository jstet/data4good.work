    <h2 id="definition">What qualifies as a Data4Good Organization?</h2>
    <p>
      <strong>Data4Good</strong> refers to the use of data for social and
      environmental good, leveraging its potential for addressing pressing
      challenges instead of solely focusing on maximizing profits. In the
      context of this website, this means applying methodologies related to Data
      Science for causes that are compatible with one or multiple
      <a href="https://sdgs.un.org/goals">Sustainable Development Goals</a>.
      Additionally, Data4Good must adhere to the ethical practices of
      prioritizing privacy and consent, promoting transparency and mitigating
      biases for fairness.
